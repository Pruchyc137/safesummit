-- 1) รายชื่อทริป seed ที่จะถูกลบ
select id, name_th, organizer_id, status, booked_count
from trips
where organizer_id in (
  select id from organizers where user_id in (
    '00000001-0000-0000-0000-000000000001','00000001-0000-0000-0000-000000000002',
    '00000001-0000-0000-0000-000000000003','00000001-0000-0000-0000-000000000004',
    '00000001-0000-0000-0000-000000000005','00000001-0000-0000-0000-000000000006',
    '00000001-0000-0000-0000-000000000007'
  )
);

-- 2) ⚠️ สำคัญที่สุด — เช็คว่ามีลูกค้าจริงจองทริป seed พวกนี้ไปแล้วหรือยัง
--    ถ้า query นี้มีแถวออกมา แปลว่ามีการจองจริงติดอยู่ — ห้ามลบทริปตรงๆ
--    (ต้องมาคุยกันก่อนว่าจะทำยังไงกับการจองเหล่านั้น)
select b.id as booking_id, b.booking_ref, b.user_id, b.trip_id, b.status, b.pay_status,
       t.name_th as trip_name, u.email as customer_email
from bookings b
join trips t on t.id = b.trip_id
left join users u on u.id = b.user_id
where b.trip_id in (
  select id from trips where organizer_id in (
    select id from organizers where user_id in (
      '00000001-0000-0000-0000-000000000001','00000001-0000-0000-0000-000000000002',
      '00000001-0000-0000-0000-000000000003','00000001-0000-0000-0000-000000000004',
      '00000001-0000-0000-0000-000000000005','00000001-0000-0000-0000-000000000006',
      '00000001-0000-0000-0000-000000000007'
    )
  )
);

